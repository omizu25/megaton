#include "locus.h"
#include "application.h"
#include <assert.h>

//==================================================
// 定義
//==================================================
const float CLocus::STD_SIZE = 30.0f;

//--------------------------------------------------
// 生成
//--------------------------------------------------
CLocus* CLocus::Create()
{
	CLocus* pLocus = nullptr;

	pLocus = new CLocus;

	if (pLocus != nullptr)
	{// nullチェック
		pLocus->Init();
	}

	return pLocus;
}

//--------------------------------------------------
// デフォルトコンストラクタ
//--------------------------------------------------
CLocus::CLocus()
{
}

//--------------------------------------------------
// デストラクタ
//--------------------------------------------------
CLocus::~CLocus()
{
}

//--------------------------------------------------
// 初期化
//--------------------------------------------------
void CLocus::Init()
{
	// 初期化
	CObject3D::Init();

	// サイズの設定
	CObject3D::SetSize(D3DXVECTOR3(STD_SIZE, STD_SIZE, 0.0f));
}

//--------------------------------------------------
// 終了
//--------------------------------------------------
void CLocus::Uninit()
{
	// 終了
	CObject3D::Uninit();
}

//--------------------------------------------------
// 更新
//--------------------------------------------------
void CLocus::Update()
{
	// 更新
	CObject3D::Update();
}

//--------------------------------------------------
// 描画
//--------------------------------------------------
void CLocus::Draw()
{
	// 描画
	CObject3D::Draw();
}

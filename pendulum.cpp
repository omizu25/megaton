CPendulum * CPendulum::Create(void)
{
	// オブジェクトインスタンス
	CPendulum *pPendulum = nullptr;

	// メモリの解放
	pPendulum = new CPendulum;

	if (pPendulum != nullptr)
	{// 数値の初期化
		pPendulum->Init();
	}
	else
	{// メモリの確保ができなかった
		assert(false);
	}

	// インスタンスを返す
	return pPendulum;
}


#include "bg.h"
#include <assert.h>

//--------------------------------------------------
// 生成
//--------------------------------------------------
CBG* CBG::Create(const D3DXVECTOR3& pos, const D3DXVECTOR3& size)
{
	CBG* pBG = nullptr;

	pBG = new CBG;

	if (pBG != nullptr)
	{// nullチェック
		pBG->Init();
		pBG->SetPos(pos);
		pBG->SetSize(size);
	}

	return pBG;
}

//--------------------------------------------------
// デフォルトコンストラクタ
//--------------------------------------------------
CBG::CBG()
{
}

//--------------------------------------------------
// デストラクタ
//--------------------------------------------------
CBG::~CBG()
{
}

//--------------------------------------------------
// 初期化
//--------------------------------------------------
void CBG::Init()
{
	// 初期化
	CObject3D::Init();

	// テクスチャの設定
	CObject3D::SetTexture(CTexture::LABEL_BG);

	// 色の設定
	CObject3D::SetCol(D3DXCOLOR(0.5f, 0.25f, 1.0f, 0.8f));

	// キープの設定
	CObject::SetKeep(true);
}

//--------------------------------------------------
// 終了
//--------------------------------------------------
void CBG::Uninit()
{
	// 終了
	CObject3D::Uninit();
}

//--------------------------------------------------
// 更新
//--------------------------------------------------
void CBG::Update()
{
	// 更新
	CObject3D::Update();
}

//--------------------------------------------------
// 描画
//--------------------------------------------------
void CBG::Draw()
{
	// 描画
	CObject3D::Draw();
}
